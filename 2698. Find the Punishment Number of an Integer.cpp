class Solution {
public:
    int punishmentNumber(int n) {
        vector<int> v={1, 81, 100, 1296, 2025, 3025, 6724, 8281, 9801, 10000, 55225, 88209, 136161, 136900, 143641, 171396, 431649, 455625, 494209, 571536, 627264, 826281, 842724, 893025, 929296, 980100, 982081, 998001, 1000000, 1679616, 2896804, 3175524, 4941729, 7441984};
        int ans=0;
        for(int i=0;i<v.size();i++)
        {
            int x=sqrt(v[i]);
            if(x<=n)
                ans+=v[i];
        }
        return ans;
    }
};